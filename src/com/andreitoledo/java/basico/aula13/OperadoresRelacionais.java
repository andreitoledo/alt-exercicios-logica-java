package com.andreitoledo.java.basico.aula13;

public class OperadoresRelacionais {

	/*
	 * == igual a 
	 * != diferente de 
	 * > maior que 
	 * < menor que 
	 * >= maior ou igual que
	 * <= menor ou igual que
	 */

	public static void main(String[] args) {

		int valor1 = 1;
		int valor2 = 2;		
		
		System.out.println("valor 1 == valor2: " + (valor1 == valor2));
		System.out.println("valor 1 != valor2: " + (valor1 != valor2));
		System.out.println("valor 1 > valor2: " + (valor1 > valor2));
		System.out.println("valor 1 >= valor2: " + (valor1 >= valor2));
		System.out.println("valor 1 < valor2: " + (valor1 < valor2));
		System.out.println("valor 1 <= valor2: " + (valor1 <= valor2));
		

	}

}
